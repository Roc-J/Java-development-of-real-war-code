/**
 * 演示字符类型与整数类型的通用赋值方式
 * @author Roc-J
 *
 */
public class CharandInt {

	public static void main(String[] args) {
		char p=24336,p2=33156;
		int word = '@',word2 = 'd';
		System.out.println("unicode表中编码24336的字符是: " +p);
		System.out.println("unicode表中编码33156的字符是: " +p2);
		System.out.println("d在unicode编码是: " + word2);
		System.out.println("@在unicode编码是: " + word);
		
	}

}
