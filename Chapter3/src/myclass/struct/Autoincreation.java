package myclass.struct;

/**
 * 输出自增与自减操作符作为前缀与后缀的表达式结果。
 * @author Roc-J
 *
 */
public class Autoincreation {

	public static void main(String[] args) {
		int number = 10;
		System.out.println("number  :" + number);
		System.out.println("number++ :" + number++);
		System.out.println("number--  :" + number--);
		System.out.println("++number :" + ++number);
		System.out.println("--number :" + --number);

	}

}
