/**
 * 输出元的面积
 * @author Roc-J
 *
 */
public class PrintCircleArea {

	public static void main(String[] args) {
		double r = 5.0;
		final double PI = 3.1415926;
		double area = PI*r*r;
		System.out.println("半径是  " + r);
		System.out.println("圆的面积是 "+ area);

	}

}
