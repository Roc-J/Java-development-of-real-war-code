package myclass.test;

/**
 * 根据长方形长和宽输出面积
 * @author Roc-J
 *
 */
public class PrintArea {

	public static void main(String[] args) {
		double width = 4.5;
		double length = 6.4;
		double area = width * length;
		System.out.println("长是: " + length);
		System.out.println("宽是: " + width);
		System.out.println("面积是: " + area);

	}

}
