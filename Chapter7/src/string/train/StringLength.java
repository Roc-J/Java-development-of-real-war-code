package string.train;

/**
 * 演示一下字符串的 长度
 * @author Roc-J
 *
 */
public class StringLength {

	public static void main(String[] args) {
		String name="北京邮电大学";
		String address = "北京市海淀区";
		String location = name+"是位于"+address;
		System.out.println("计算机大学"+name+"的长度是"+name.length());
		System.out.println(location+"的长度是:"+location.length());

	}

}
